abstract class Operations {
  static double add(double number_1, double number_2) {
    return number_1 + number_2;
  }

  static double subtraction(double number_1, double number_2) {
    return number_1 - number_2;
  }

  static double multiplication(double number_1, double number_2) {
    return number_1 * number_2;
  }

  static double division(double number_1, double number_2) {
    return number_1 / number_2;
  }
}
